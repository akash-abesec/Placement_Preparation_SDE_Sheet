#  Problem :-https://leetcode.com/problems/merge-sorted-array/description/

# Solution :- https://www.youtube.com/watch?v=n7uwj04E0I4&ab_channel=takeUforward

class Solution:
    def merge(self, nums1: List[int], m: int, nums2: List[int], n: int) -> None:
        i = m - 1
        j = n - 1
        k = m + n - 1

        while j >= 0:
            if i >= 0 and nums1[i] > nums2[j]:
                nums1[k] = nums1[i]
                i -= 1
            else:
                nums1[k] = nums2[j]
                j -= 1
            k -= 1
